    public void readJTable(){
        DefaultTableModel modelo = (DefaultTableModel) jtClientes.getModel();
        modelo.setNumRows(0);
        ClienteDao cDao = new ClienteDao();
        
        for(Cliente c: cDao.Read()){
            
            modelo.addRow(new Object[]{
                c.getId(),
                c.getNome(),
                c.getSexo(),
                df.format(c.getDataNasc()),
                c.getEstado(),
                c.getCidade(),
                c.getBairro(),
                c.getRua(),
                c.getNumero(),
                c.getTelefone()
            });
           
        }

    }
